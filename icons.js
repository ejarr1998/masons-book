// Returns an inline <svg> string sized by CSS (see the icon-sizing rules in
// style.css) — width/height are intentionally not set here.
export function icon(name) {
  const def = ICONS[name];
  if (!def) return "";
  const attrs = def.filled
    ? 'fill="currentColor" stroke="none"'
    : 'fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"';
  return `<svg viewBox="0 0 24 24" ${attrs} aria-hidden="true">${def.body}</svg>`;
}
